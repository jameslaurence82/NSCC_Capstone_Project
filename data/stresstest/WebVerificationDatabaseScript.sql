SELECT DRUG_ID, DRUG_Name
FROM DRUG
WHERE DRUG_ID = 'DB00019' OR DRUG_ID = 'DB00570'
SELECT FK_DRUG_ID, PRO_GenericName
FROM PRODUCT
WHERE FK_DRUG_ID = 'DB00570' OR FK_DRUG_ID = 'DB00019'
SELECT FK_DRUG_ID, INTER_DrugID, INTER_Description
FROM DRUG_INTERACTION
WHERE FK_DRUG_ID = 'DB00570' AND INTER_DrugID = 'DB00019'
ORDER BY FK_DRUG_ID
SELECT FK_DRUG_ID, FOOD_Description 
FROM FOOD_INTERACTION
WHERE FK_DRUG_ID = 'DB00570'